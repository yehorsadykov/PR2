// Підключаємо простір імен для роботи з регулярними виразами
using System.Text.RegularExpressions; 

class Program {
    static void Main() {
        Console.Write("Введіть логін: "); // Запитуємо у користувача введення логіна
        string login = Console.ReadLine(); // Зчитуємо введений користувачем логін
    
        // Викликаємо метод для перевірки логіна
        if (IsValidLogin(login)) {
            // Якщо логін коректний, виводимо відповідне повідомлення
            Console.WriteLine("Логін коректний!");
        } else {
            // Якщо логін некоректний, виводимо відповідне повідомлення
            Console.WriteLine("Логін некоректний.");
        }
    }

    // Метод перевіряє, чи відповідає логін правилам
    static bool IsValidLogin(string login) {
        // Створюємо регулярний вираз для перевірки логіна
        // ^[A-Za-z] — логін повинен починатися з літери (велика або мала)
        // [A-Za-z0-9]{1,9}$ — після першої літери можуть бути від 1 до 9 символів, які є літерами або цифрами
        Regex regex = new Regex(@"^[A-Za-z][A-Za-z0-9]{1,9}$");
        
        // Перевіряємо, чи відповідає логін регулярному виразу, і повертаємо результат
        return regex.IsMatch(login);
    }
}
