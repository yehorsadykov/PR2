// Підключаємо простір імен для роботи з регулярними виразами
using System.Text.RegularExpressions; 

class Program {
    static void Main() {
        // Оригінальний рядок, в якому будемо шукати і замінювати слова
        string input = "Василь любить читати, але Василя не любить читати.";
        // Викликаємо метод ReplaceWords для заміни визначених слів у рядку
        string result = ReplaceWords(input);
        // Виводимо оригінальний рядок
        Console.WriteLine("Оригінальний рядок: " + input);
        // Виводимо рядок після заміни слів
        Console.WriteLine("Рядок після заміни: " + result);
    }

    // Метод для заміни визначених слів у рядку
    static string ReplaceWords(string input) {
        // Створюємо регулярний вираз для пошуку всіх можливих форм слова "Василь"
        // \b — позначає межу слова, щоб уникнути часткових збігів
        // Василь(?:я|ю|ем|у|е)? — шукаємо слово "Василь" і дозволяємо його різні форми: Василя, Василю, Василем тощо
        // ? — означає, що закінчення (я, ю, е тощо) можуть бути присутні або відсутні
        // RegexOptions.IgnoreCase — ігноруємо регістр, щоб знайти слово незалежно від великих чи малих літер
        Regex regex = new Regex(@"\bВасиль(?:я|ю|ем|ю|у|е)?\b", RegexOptions.IgnoreCase);
        // Заміна всіх знайдених форм "Василь" на "Петро"
        return regex.Replace(input, "Петро");
    }
}